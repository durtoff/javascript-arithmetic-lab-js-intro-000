function add(a, b) {
  add = a + b
  return add
}

function subtract(a, b) {
  add = a - b
  return add
}

function multiply(a, b) {
  add = a * b
  return add
}

function divide(a, b) {
  add = a / b
  return add
}

function inc(n) {
  inc = ++n
  return inc
}

function dec(n) {
  inc = --n
  return inc
}

function makeInt(n) {
  makeInt('n')
  return makeInt.integer()
}
